mystring = 'abcdefghijk'
# to show a particular element of string
# negative number just counts string from the last element
print(mystring[2])
# To grab section of string we can use ":"
print(mystring[2:])
# End of the section can be written after column
print(mystring[2:4])
# A third number states how many elements should it jump
print(mystring[::2])
# To reverse a string one can use
print(mystring[::-1])